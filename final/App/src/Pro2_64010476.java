public class Pro2_64010476 {
    public static void main(String[] args) throws Exception {
        int BMI = 90;
        switch( BMI ) {
            case 90 : System.out.println( BMI + " = A"); break;
            case 80 : System.out.println( BMI + " = B"); break;
            case 70 : System.out.println( BMI + " = C"); break;
            case 60 : System.out.println( BMI + " = D"); break;
            default : System.out.println( BMI + " = F" );
        }
    }
}
